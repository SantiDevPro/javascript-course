let number1 = 3;
let number2 = 5;
// Incremento
let result = number1++;
console.log(result);
// Decremento
result = number1--;
console.log(result);
// Suma
result = number1 + number2;
console.log(result);
// Resta
result = number2 - number1;
console.log(result);
// Multiplicacion
result = number1 * number2;
console.log(result);
// Division
result = number1 / number2;
console.log(result);
// Exponenciacion
result = number2 ** number1;
console.log(result);
// Resto
result = number1 % number2;
console.log(result);
// Unary negation
result = -number2;
console.log(result);
// Unary plus
result = +number2;
console.log(result);
