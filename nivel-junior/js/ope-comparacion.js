let number1 = 5;
let number2 = 7;
// Equality
let result = number1 == number2;
console.log(result);
// Inequality
result = number1 != number2;
console.log(result);
// Identity
result = number1 === number2;
console.log(result);
// Non-identity
result = number1 !== number2;
console.log(result);
// Greater than
result = number1 > number2;
console.log(result);
// Greater than or equal
result = number1 >= number2;
console.log(result);
// Less than
result = number1 < number2;
console.log(result);
// Less than or equal
result = number1 <= number2;
console.log(result);
