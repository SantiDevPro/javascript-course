// Metodos:

// sqrt()
let number = Math.sqrt(25);
console.log(number);
// cbrt()
number = Math.cbrt(27);
console.log(number);
// max()
number = Math.max(15, 11, 45, 67, 30, 21);
console.log(number);
// min()
number = Math.min(27, 87, 95, 17, 13, 11);
console.log(number);
// random()
number = Math.random();
console.log(number);
// round()
number = Math.round(5.4);
console.log(number);
// fround()
number = Math.fround(9.7555555555);
console.log(number);
// floor()
number = Math.floor(27.8);
console.log(number);
// trunc()
number = Math.trunc(25.123);
console.log(number);

