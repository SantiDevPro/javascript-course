let num1 = 5;
let num2 = 9;
// If
if (num1 < num2) {
  console.log("5 menor a 9");
}
// Else
if (num2 > num1) {
  console.log("9 es mayor a 5");
} else {
  console.log("No es mayor");
}
// Else-if
if (num1 > num2) {
  console.log("Si es mayor");
} else if (num1 < num2) {
  console.log("Es menor");
} else {
  console.log("Son iguales");
}
